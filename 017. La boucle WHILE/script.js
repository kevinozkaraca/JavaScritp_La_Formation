//while
console.log(`boucle while...`);
let n = 0;
let x = 1;
while (n < 5) {
    n++;
    x += n;
    console.log(`... n est égale à ${n}`);
}

// do...while
console.log(`boucle while...`);
let i = 0;
do {
    i += 1;
    console.log(`... i est égale à ${i}`);
} while (i < 5);